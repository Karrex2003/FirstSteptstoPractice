//Write a Java program that takes a number as input and prints its multiplication table up to 10.
//Input a number: 8
public class tablas {
    public static void main(String[] args) {
        for (int i = 1; i<=10; i++){
            System.out.printf("%d * %d = %d ", 8, i, 8 * i);
        }
    }
}
